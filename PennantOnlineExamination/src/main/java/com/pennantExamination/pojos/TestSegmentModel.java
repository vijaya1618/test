package com.pennantExamination.pojos;

public class TestSegmentModel {
	
	protected int testsegmentId;
	protected String testsegmentName;
	protected int Enabled;
	public int getTestsegmentId() {
		return testsegmentId;
	}
	public void setTestsegmentId(int testsegmentId) {
		this.testsegmentId = testsegmentId;
	}
	public String getTestsegmentName() {
		return testsegmentName;
	}
	public void setTestsegmentName(String testsegmentName) {
		this.testsegmentName = testsegmentName;
	}
	public int getEnabled() {
		return Enabled;
	}
	public void setEnabled(int enabled) {
		Enabled = enabled;
	}
	
	

}
